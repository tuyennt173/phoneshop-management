    private void tblBangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBangMouseClicked
        int row = tblBang.getSelectedRow();
        if (row < 0) {
            return;
        }

        dcmNV.setSelectedItem(tblBang.getValueAt(row, 1));
        dcmNPP.setSelectedItem(tblBang.getValueAt(row, 0));
        txtma.setText(tblBang.getValueAt(row, 2).toString());
        txtGia.setText(tblBang.getValueAt(row, 3).toString());
    }//GEN-LAST:event_tblBangMouseClicked

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tblBang.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Chọn dòng cần sửa");
            return;
        }
        try {
            PhieuNhapModel cv = getFormData();
            if (cv == null) {
                return;
          }
            String ma = tblBang.getValueAt(row, 2).toString();
            cv.setMaPN(ma);
            if (pns.updatePN(cv)!= null) {
                JOptionPane.showMessageDialog(this, "Sửa thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Sửa thất bại");
            }
        } catch (Exception e) {
        }

        loadTable();
        clearForm();
    }//GEN-LAST:event_btnSuaActionPerformed

