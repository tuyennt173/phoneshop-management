    private void loadComboNPP() {
        ArrayList<NhaPhanPhoiModel> list = npp.getAllNPP();
        Collections.sort(list, Comparator.comparing(NhaPhanPhoiModel -> NhaPhanPhoiModel.getMa()));
        for (NhaPhanPhoiModel x : list) {
            dcmNPP.addElement(new NhaPhanPhoi(x.getId(), x.getMa(), x.getTen(), x.getDiaChi(), x.getEmail(), x.getSdt(), x.getNgayTao(), x.getNgaySua()));
        }
    }

    private void loadComboNV() {
        ArrayList<NhanVienModel> list = nvs.getAllNV();
        Collections.sort(list, Comparator.comparing(NhanVienModel -> NhanVienModel.getMa()));
        for (NhanVienModel x : list) {
            dcmNV.addElement(new NhanVien(x.getId(),x.getMa(), x.getHoTen()));
        }
    }

    public void loadTable() {
        ArrayList<PhieuNhapModel> list = pns.getAllPhieuNhap();
        dtm.setRowCount(0);

        for (PhieuNhapModel x : list) {
             Object[]rowData={
               x.getNhaPhanPhoi(),x.getNhanVien(),x.getMaPN(),x.getTongGia(),x.getNgayTao()
             };
            dtm.addRow(rowData);
        }
    }

    private PhieuNhapModel getFormData() {
        NhanVien nv = (NhanVien) cbbNV.getSelectedItem();
        NhaPhanPhoi pn = (NhaPhanPhoi) cbbNPP.getSelectedItem();
        String ma = txtma.getText().trim();
        float gia;

        if (ma.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã");
            txtma.requestFocus();
            return null;
        }
        if (txtGia.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã");
            txtma.requestFocus();
            return null;
        }else{
           gia=Float.parseFloat(txtGia.getText().trim());
            try {
                
            if(gia<0){
                JOptionPane.showMessageDialog(this, "giá k đc để trống");
            } 
            } catch (NumberFormatException e) {
            }
        }
       
        return new PhieuNhapModel(null, pn, nv, ma, gia, null);
    }

