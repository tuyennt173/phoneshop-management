    private void loadCBB() {
        ArrayList<NSXModel> listNSX = iNSXService.getAllNSX();
        for (NSXModel x : listNSX) {
            dcbm.addElement(new NSX(x.getId(), x.getMa(), x.getTen(), null, null));
        }
    }

    private void loadData() {
        ArrayList<SanPhamModel> listSP = iSanPhamService.getAllSanPham();
        dtm.setRowCount(0);
        for (SanPhamModel s : listSP) {
            dtm.addRow(new Object[]{
                s.getMa(),
                s.getTen(),
                s.getNsx(),
                s.getMoTa()
            });
        }
    }

    private SanPhamModel getSPFormInput() {
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String moTa = txtMoTa.getText();
        NSX nsx = (NSX) cbbNSX.getSelectedItem();
    
        if (ma.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã không được để trống");
            return null;
        }
        if (ten.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên không được để trống");
            return null;
        }
        return new SanPhamModel(null, nsx, ma, ten, moTa, null, null);
    }

