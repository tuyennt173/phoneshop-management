    private void tbnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbnSuaActionPerformed
        int index = tblHienThi.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Mời bạn chọn dòng cần sửa!");
            return;
        }
        SanPhamModel sp = getSPFormInput();
        ArrayList<SanPhamModel> list = iSanPhamService.getAllSanPham();
        for (SanPhamModel x : list) {
            if (x.getMa() != null && x.getMa().equals(tblHienThi.getValueAt(index, 0).toString())) {
                sp.setId(x.getId());
                continue;
            }
            if (x.getMa() != null && x.getMa().equals(sp.getMa())) {
                JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
                return;
            }
        }
        if (JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không", "Thông báo", JOptionPane.YES_NO_OPTION) != JOptionPane.YES_OPTION) {
            return;
        }
        if (iSanPhamService.updateSP(sp) != null) {
            JOptionPane.showMessageDialog(this, "Update thành công");
            loadData();
        } else {
            JOptionPane.showMessageDialog(this, "Update thất bại");
        }
    }//GEN-LAST:event_tbnSuaActionPerformed

    private void btnImportEXActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportEXActionPerformed
        JFileChooser jfile = new JFileChooser("D:\\duan1_nhom8\\excel");
        int batchSize = 20;
        Connection con = null;
        int x1 = tblHienThi.getRowCount();
        try {
            int f = jfile.showOpenDialog(this);
            File file = jfile.getSelectedFile();
           
            con = DB_Context.getConnection();
            con.setAutoCommit(false);
            String sql = "INSERT INTO SANPHAM VALUES(NEWID(),?,?,?,?,GETDATE(),null)";

            PreparedStatement state = con.prepareStatement(sql);

            BufferedReader lineReader = new BufferedReader(new FileReader(file));

            String lineText = null;
            int count = 0;

            lineReader.readLine();
            while ((lineText = lineReader.readLine()) != null) {
                String[] data = lineText.split(",");

                String NSX = data[1];
                String Ma = data[2];
                String Ten = data[3];
                String MT = data[4];

                state.setString(1, NSX);
                state.setString(2, Ma);
                state.setString(3, Ten);
                state.setString(4, MT);
                state.addBatch();
                if (count % batchSize == 0) {
                    state.executeBatch();
                }
            }

            lineReader.close();
            state.executeBatch();
            con.commit();
            con.close();
            JOptionPane.showMessageDialog(this, "Thêm từ danh sách thành công !!");
            loadData();

        } catch (Exception e) {
        }
        int x2 = tblHienThi.getRowCount();
        if (x1 == x2) {
            JOptionPane.showMessageDialog(this, "Thêm không thành công !!");
            return;
        }
    }//GEN-LAST:event_btnImportEXActionPerformed

